function smtpmailer($to, $from, $from_name, $subject, $body)
    {
        $mail = new PHPMailer(true);
        $mail->IsSMTP();
        $mail->SMTPAuth = true; 
 
        $mail->SMTPSecure = 'ssl'; 
        $mail->Host = 'mail.ainmedicare.com.my';
        $mail->Port = 465;  
        $mail->Username = 'noreply@ainmedicare.com.my';
        $mail->Password = 'amsb@noreply';   
   
   //   $path = 'reseller.pdf';
   //   $mail->AddAttachment($path);
   
        $mail->IsHTML(true);
        $mail->From="noreply@ainmedicare.com.my";
        $mail->FromName=$from_name;
        $mail->Sender=$from;
        $mail->AddReplyTo($from, $from_name);
        $mail->Subject = $subject;
        $mail->Body = $body;
        $mail->AddAddress($to);
        $mail->Send();
